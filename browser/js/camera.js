module.exports = {
	getCamera: getCamera,
	moveLeft: moveLeft,
	moveRight: moveRight,
	moveUp: moveUp,
	moveDown: moveDown, 
	lookDown: lookDown,
	lookLeft: lookLeft,
	lookRight: lookRight,
	lookUp: lookUp,
	moveForward: moveForward,
	moveBackward: moveBackward
};





