const WIDTH = require("./constants").WIDTH;
const HEIGHT = require("./constants").HEIGHT;
const ASPECT = WIDTH / HEIGHT;
let PI = Math.PI;

let positionX = 0;
let positionY = 0;
let positionZ = 100;

let rotationX = 0;
let rotationY = 0;
let rotationZ = 0;
let fiveDegrees = (((PI/2)/3)/3)/2;


let camera = new THREE.PerspectiveCamera(60, ASPECT, 1, 10000);

camera.position.set(positionX, positionY, positionZ);
camera.rotation.set(rotationX, rotationY, rotationZ);

function getCamera() {
	return camera;
}
function moveLeft() {
	camera.position.x-=3;
}
function moveRight() {
	camera.position.x+=3;
}
function moveUp() {
	camera.position.y+=3;
}
function moveDown() {
	camera.position.y-=3;
}
function lookDown() {
	camera.rotation.x-=fiveDegrees;
}
function lookUp() {
	camera.rotation.x+=fiveDegrees;
}
function lookLeft() {
	camera.rotation.y+=fiveDegrees;
}
function lookRight() {
	camera.rotation.y-=fiveDegrees;
}
function moveForward(){
	camera.position.z-=3;
}
function moveBackward(){
	camera.position.z+=3;
}
